package Kuis_Iftitah;

/**
 *
 * @author Iftitah Hidayati
 */
public class Mahasiswa {

    String nim;
    String nama;
    String jurusan;
    String prodi;
    double ipk;

    Mahasiswa(String nim, String nama, String jurusan, String prodi, double ipk) {
        this.nim = nim;
        this.nama = nama;
        this.jurusan = jurusan;
        this.prodi = prodi;
        this.ipk = ipk;
    }
}
